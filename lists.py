fruits = ["pawpaw","orange","mango","cashew"]
print(fruits)        # this prints the entire list
print(fruits[0])     #this prints the first item on the list
print(fruits[-1])    #reads the last item on the list
print(fruits[0:3])   # list the index from 0 - 3
fruits[0] = "papaya" #changed the first item in the list
print(fruits)        # this prints the list with the updated "papaya" instead of "pawpaw"